#  Desenvolva um programa em Python que receba duas notas de um aluno e calcule a média aritmética.
print('==============================')
nota1 = input('Insira o valor da nota1:')
nota1 = int(nota1)
print('==============================')
nota2 = input('Insira o valor da nota2:')
nota2 = int(nota2)
print('==============================')

media = (nota1 + nota2) / 2

print("A média do aluno é:", media)
print('==============================')